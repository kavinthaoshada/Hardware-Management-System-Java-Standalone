    public void loadBrands(){
        try{
            
            ResultSet rs = MySQL.search("SELECT * FROM `brand` ORDER BY `brand`.`id` DESC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                dtm.addRow(v);
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    public void loadBrands(String text){
        try{
            
            ResultSet rs = MySQL.search("SELECT * FROM `brand` WHERE `brand`.`name` LIKE '%"+text+"%' ORDER BY `brand`.`id` DESC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                dtm.addRow(v);
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    

