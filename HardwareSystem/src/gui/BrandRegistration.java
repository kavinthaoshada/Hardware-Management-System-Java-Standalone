    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        if(name.isEmpty()){
            JOptionPane.showMessageDialog(this, "please enter brand name", "warning", JOptionPane.WARNING_MESSAGE);
        }else{
            try{
                ResultSet rs = MySQL.search("SELECT * FROM `brand` WHERE `name`='"+name+"'");
                
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "brand name alreay exist", "warning", JOptionPane.WARNING_MESSAGE);
                }else{
                    MySQL.iud("INSERT INTO `brand`(`name`) VALUE('"+name+"')");
                    loadBrands();
                    
                    if(this.pr != null){
                        this.pr.loadBrands();
                    }else if(this.pr1 != null){
                        this.pr1.loadBrands();
                    }
                    
                    
                    jTextField1.setText("");
                    jTextField1.grabFocus();
                    JOptionPane.showMessageDialog(this, "success", "success", JOptionPane.INFORMATION_MESSAGE);
                }
                
                
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

